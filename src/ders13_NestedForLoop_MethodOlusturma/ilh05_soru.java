package ders13_NestedForLoop_MethodOlusturma;

public class ilh05_soru {

    public static void main(String[] args) {

        // 1 den 10 na kadar sayilari sirala

        for (int i = 0; i <=10 ; i++) {

            System.out.print(i+" ");
        }

        System.out.println("");

        // 2 haneli rakamlari cift sayilari sirala

        for (int i = 10; i <=100 ; i+=2) {

            System.out.print(i+" ");

        }


    }
}
