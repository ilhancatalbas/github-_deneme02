public class C03_NestedForLoop {

    public static void main(String[] args) {

        // 1-4 arasindaki sayilar icin carpim tablosu olusturalim

        for (int i = 1; i <= 4; i++) {

            for (int j = 1; j <= 4; j++) {

            }

            System.out.println(i*i + " ");

        }




    }
}
